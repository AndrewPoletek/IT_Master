public class PlanetWeight {
    public static void main(String[] arguments){
        int weight = 80;
        System.out.print("Waga astronauty na ziemii:");
        System.out.println(weight);
        double wenus = weight*0.907;
        System.out.print("Waga astronauty na wenus:");
        System.out.println(wenus);
        double uran = weight*0.889;
        System.out.print("Waga astronauty na uranie:");
        System.out.println(uran);
    }
}
